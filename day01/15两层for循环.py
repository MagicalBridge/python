# 下面这段代码写的规则是两层的for循环
# 两层for循环的逻辑可以这样的理解 ，外层每执行一次内层就执行10次
# 但是在这个程序中我做了一个限制，当内层循环的变量大于4的时候跳出内层
# 循环，实际上执行循环的总次数是 10*5 = 50次
for i in range(3):
    print("__________", i)
    for j in range(10):
        print(j)
        if j > 4:
            break
